void main() {
  int a=10;
  int b=20;
  var res;
  print("Value of a is  $a and b is $b");
  //addition
  res= a+b;
  print("Addition result  $res");
  //subtraction
  res= a-b;
  print("Subtraction result  $res");
  //multiplication
  res= a*b;
  print("Multiplication result  $res");
  //division
  res= a/b;
  print("Division result  $res");
  //division returning integer result
  res= a~/b;
  print("Division returning integer  $res");
  //modulus
  res = a%b; 
  print("Modulus result $res"); 
  //increment 
  print("Value of a is  $a");
  a++; 
  print("Incrementing a gives $a"); 
 //decrement 
  print("Value of b is  $b");
  b--; 
  print("Decrementing b gives $b"); 
}
